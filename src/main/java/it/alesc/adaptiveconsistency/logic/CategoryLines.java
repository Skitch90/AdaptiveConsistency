package it.alesc.adaptiveconsistency.logic;

/**
 * Its values are used to distinguish between groups of lines.
 * 
 * @author Alessandro Schio
 * @version 1.0 17 Dec 2013
 * 
 */
public enum CategoryLines {
	/**
	 * <code>Variables</code> value indicates the variables group
	 */
	VARIABLES,
	/**
	 * <code>Constraints</code> value indicates the constraints group
	 */
	CONSTRAINTS,
	/**
	 * <code>OrderLine</code> value indicates the order line
	 */
	ORDER_LINE
}
